import json
from openai import OpenAI


class OpenAIExtractor:
    def __init__(self, api_key: str, model: str = "gpt-4.1-mini"):
        self.client = OpenAI(api_key=api_key)
        self.model = model

    def extract(self, pdf_path: str) -> dict:
        file_id = self._upload_file(pdf_path)
        response_text = self._request_extraction(file_id)
        return self._parse_response(response_text)

    def _upload_file(self, pdf_path: str) -> str:
        with open(pdf_path, "rb") as f:
            uploaded = self.client.files.create(
                file=f,
                purpose="user_data"
            )
        return uploaded.id

    def _request_extraction(self, file_id: str) -> str:
        response = self.client.responses.create(
            model=self.model,
            text=self._build_text_format(),
            input=[
                {
                    "role": "user",
                    "content": [
                        {"type": "input_file", "file_id": file_id},
                        {"type": "input_text", "text": self._build_prompt()}
                    ]
                }
            ]
        )
        return response.output_text

    def _parse_response(self, response_text: str) -> dict:
        return json.loads(response_text)

    def _build_text_format(self) -> dict:
        return {
             "format": {
            "type": "json_schema",
            "name": "financial_inputs_ru",
            "strict": True,
            "schema": {
                "type": "object",
                "additionalProperties": False,
                "properties": {
                    "period": {"type": ["string", "null"]},
                    "unit": {
                        "type": "string",
                        "enum": ["rub", "thousand_rub", "million_rub", "unknown"]
                    },
                    "financial_inputs": {
                        "type": "object",
                        "additionalProperties": False,
                        "properties": {
                            "total_assets": {"type": ["integer", "null"]},
                            "equity": {"type": ["integer", "null"]},
                            "long_term_liabilities": {"type": ["integer", "null"]},
                            "short_term_liabilities": {"type": ["integer", "null"]},
                            "current_assets": {"type": ["integer", "null"]},
                            "revenue": {"type": ["integer", "null"]},
                            "net_income": {"type": ["integer", "null"]},
                            "previous_year_revenue": {"type": ["integer", "null"]}
                        },
                        "required": [
                            "total_assets",
                            "equity",
                            "long_term_liabilities",
                            "short_term_liabilities",
                            "current_assets",
                            "revenue",
                            "net_income",
                            "previous_year_revenue"
                        ]
                    }
                },
                "required": ["period", "unit", "financial_inputs"]
            }
        }
    }

    def _build_prompt(self) -> str:
        return """
ЗАДАЧА
Извлеки из PDF бухгалтерской отчетности российской компании только нужные поля.
Возьми только последний доступный отчетный год.

ВЫВОД
Верни только JSON по схеме.

ПОЛЯ
1. total_assets
2. equity
3. long_term_liabilities
4. short_term_liabilities
5. current_assets
6. revenue
7. net_income
8. previous_year_revenue

КАК ИСКАТЬ ПОЛЯ

1. total_assets
Код строки: 1600
Ищи только:
- строку с кодом 1600
- "БАЛАНС"
- "Баланс"
Правила:
- Бери только строку с кодом 1600.
- Если есть слово "БАЛАНС", но код строки не 1600, не используй ее.
- Возвращай только число из строки 1600 за последний отчетный год.

2. equity
Код строки: 1300
Ищи только:
- строку с кодом 1300
- "Итого по разделу III. Капитал и резервы"
- "Итого по разделу III"
- "Капитал и резервы"
Правила:
- Бери только строку с кодом 1300.
- Не бери внутренние строки раздела III.
- Возвращай только число из строки 1300 за последний отчетный год.

3. long_term_liabilities
Код строки: 1400
Ищи только:
- строку с кодом 1400
- "Итого по разделу IV. Долгосрочные обязательства"
- "Итого по разделу IV"
- "Долгосрочные обязательства"
Правила:
- Бери только строку с кодом 1400.
- Не бери внутренние строки раздела IV.
- Возвращай только число из строки 1400 за последний отчетный год.

4. short_term_liabilities
Код строки: 1500
Ищи только:
- строку с кодом 1500
- "Итого по разделу V. Краткосрочные обязательства"
- "Итого по разделу V"
- "Краткосрочные обязательства" — только если это итог раздела V
Правила:
- Бери только строку с кодом 1500.
- Не бери отдельные строки внутри раздела V.
- Не бери "Заемные средства".
- Не бери "Кредиторская задолженность".
- Не бери "Доходы будущих периодов".
- Не бери "Оценочные обязательства".
- Не бери "Прочие обязательства".
- Не бери никакие промежуточные строки.
- Нужна только итоговая сумма по всему разделу V.
- Если на странице есть несколько строк с обязательствами, выбери только строку, где есть код 1500.
- Если не можешь точно определить строку 1500, верни null.
- Возвращай только число из строки 1500 без изменений.

5. current_assets
Код строки: 1200
Ищи только:
- строку с кодом 1200
- "Итого по разделу II. Оборотные активы"
- "Итого по разделу II"
- "Оборотные активы"
Правила:
- Бери только строку с кодом 1200.
- Не бери внутренние строки раздела II.
- Возвращай только число из строки 1200 за последний отчетный год.

6. revenue
Код строки: 2110
Ищи только:
- строку с кодом 2110
- "Выручка"
Правила:
- Бери только строку с кодом 2110.
- Не бери другие строки доходов.
- Возвращай только число из строки 2110 за последний отчетный год.

7. net_income
Код строки: 2400
Ищи только:
- строку с кодом 2400
- "Чистая прибыль (убыток)"
- "Чистая прибыль"
Правила:
- Бери только строку с кодом 2400.
- Не бери "Прибыль до налогообложения".
- Не бери другие промежуточные финансовые результаты.
- Возвращай только число из строки 2400 за последний отчетный год.

8. previous_year_revenue
Код строки: 2110
Ищи только:
- строку с кодом 2110
- "Выручка"
Правила:
- Бери значение строки 2110 за год, который идет перед последним отчетным годом.
- Если за предыдущий год стоит прочерк, верни null.
- Не бери другие строки доходов.
- Не подставляй значения из пояснений, если в самой форме по строке 2110 стоит прочерк.

ОБЩИЕ ПРАВИЛА
- Ищи только строки из списка выше.
- Сначала ориентируйся на код строки.
- Если код строки найден, используй именно его.
- Название строки используй как дополнительную проверку.
- Бери только последний доступный отчетный год.
- Для previous_year_revenue бери предыдущий год относительно последнего.
- Возвращай только число из таблицы.
- Ничего не пересчитывай.
- Ничего не дели.
- Ничего не умножай.
- Не меняй масштаб.
- Не округляй.
- Не добавляй десятичную точку.
- Если в таблице число записано как "2 730 160 826", верни 2730160826.
- Если указано "Единица измерения: тыс. руб.", верни unit = "thousand_rub".
- Если указано "Единица измерения: руб.", верни unit = "rub".
- Если указано "Единица измерения: млн руб.", верни unit = "million_rub".
- Если единицу измерения определить нельзя, верни unit = "unknown".
- Если поле не найдено точно, верни null.
- Если стоит прочерк, верни null.
- Если число стоит в скобках, то оно отрицательное.

ПРОВЕРКА ПЕРЕД ОТВЕТОМ
- Убедись, что возвращаешь только JSON.
- Убедись, что все числовые поля либо integer, либо null.
- Убедись, что unit заполнен.
- Убедись, что для баланса используются коды 1200, 1300, 1400, 1500, 1600.
- Убедись, что для отчета о финансовых результатах используются коды 2110 и 2400.
"""